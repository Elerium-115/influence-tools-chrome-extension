        if (lotCrewId) {
            locationControllerInfo.lot.delegatedToAddress = crewDataByCrewId[lotCrewId].delegatedToAddress;
            locationControllerInfo.lot.delegatedToName = crewDataByCrewId[lotCrewId].delegatedToName;
        }
